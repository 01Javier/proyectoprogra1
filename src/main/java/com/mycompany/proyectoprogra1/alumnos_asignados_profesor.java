    private void Btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_buscarActionPerformed
         String nombreCurso = jComboBox1.getSelectedItem().toString();
        
                
        for(Curso c : Proyectoprogra1.cursos){
            if(c.toString().equals(nombreCurso)){
                cursoSeleccionado = c;
                break;
            }else{
                System.out.println("No se asignaron anterirormente");//verificar si ya se asigno anteriormente
            }
        }   
        if(cursoSeleccionado !=null){
            DefaultTableModel data = new DefaultTableModel(new String[]{"Carnet","Nombre","Apellido","Nota"},cursoSeleccionado.getAlumnos().size());
            jTable1.setModel(data);

            int row = 0;

            for(Alumno e : cursoSeleccionado.getAlumnos()){
                jTable1.setValueAt(e.getCarnet(), row, 0);
                jTable1.setValueAt(e.getNombre(), row, 1);
                jTable1.setValueAt(e.getApellido(), row, 2);
                jTable1.setValueAt(e.getNota(), row, 3);
                row ++;
            }
        }
    }//GEN-LAST:event_Btn_buscarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String notaTexto = jTextField1.getText();
        
        int nota;
        try {
            nota = Integer.parseInt(notaTexto);
        } catch (NumberFormatException e) {
            System.err.println("La nota ingresada no es v√°lida.");
            return;
        }
        
        int row = jTable1.getSelectedRow();
        String name = jTable1.getModel().getValueAt(row, 0).toString();
        
        for(Alumno s : cursoSeleccionado.getAlumnos()){
            if(s.getCarnet().equals(name)){
                s.setNota(nota);
            }
        }
        actualizarDatos();
        System.out.println("Nota subida exitosamente para el alumno: "+name);//comprovacion
        JOptionPane.showMessageDialog(this, "Nota Asignada");
    }//GEN-LAST:event_jButton1ActionPerformed

