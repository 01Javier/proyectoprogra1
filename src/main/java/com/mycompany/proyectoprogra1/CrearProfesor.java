    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        Profesor p = new Profesor();
        p.nombre = ingresar_nombre.getText();
        p.apellido = ingresar_apellido.getText();
        String usuario = ingresar_usuario.getText();
        String pass = ingresar_pass.getText();

        if (validarContraseña(pass)&&validarUsuario(usuario)) {
            p.usuario = usuario;
            p.password = pass;
            Proyectoprogra1.profesores.add(p);
            JOptionPane.showMessageDialog(this, "Profesor creado.");
            ingresar_nombre.setText("");
            ingresar_apellido.setText("");
            ingresar_usuario.setText("");
            ingresar_pass.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "El usuario o la contraseña no cumple con los requisitos.");
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

