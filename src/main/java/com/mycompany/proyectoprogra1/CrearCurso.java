    private void btn_guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar1ActionPerformed
        Curso c = new Curso();
        c.setNombre(ingresar_nc.getText());
        c.setSeccion(ingresar_sec.getText());
        c.setFecha_inicio(ingresar_fei.getText());
        c.setFecha_fin(ingresar_fin.getText());
        c.setH_inicio(ingresar_hin.getText());
        c.setH_fin(ingresar_hfin.getText());
        c.setProfesor(jComboBox1.getSelectedItem().toString());
        
        Proyectoprogra1.cursos.add(c);
        JOptionPane.showMessageDialog(this, "Curso Creado.");

        ingresar_nc.setText("");
        ingresar_sec.setText("");
        ingresar_fei.setText("");
        ingresar_fin.setText("");
        ingresar_hin.setText("");
        ingresar_hfin.setText("");
    }//GEN-LAST:event_btn_guardar1ActionPerformed

